package Java_Programing_Practice;

public class Bestfriend {
	private String friendname;
	private String address;
	private String contactnumber;
	
	public String getfriendname() { // getters
		return friendname;	
	}
	public void setfriendname(String friendname) { //setters
		this.friendname=friendname;
	}
	
	public String getaddress() { // getters 
		return address;
	}
	
	public void setaddress(String address) { //setters
		this.address=address;
	}

	public String getcontactnumber() { //getters
		return contactnumber;
	}
	
	public void setcontactnumber(String contactnumber) { //setters
		this.contactnumber=contactnumber;
	}
		
}
